// valores de string podem ser definidos usando as aspas simples ou duplas
let myString1 = "Je suis au niveau de base en JS";
console.log(myString1);

//ou
let myString2 = "Hello World!";
console.log(myString2);

//podemos usar crase (acento grave) para definir uma string
let myString3 = `Je suis en mode superior`;
console.log(myString3);

//podemos usar aspas simples ou duplas dentro de uma string definida com crase:
let fullName = `Jhon "Amazing" friend`;
console.log(fullName);

//podemos definir varias strings em uma unica linha, separando-as com virgulas
let firstName = "Jhon",
  lastName = "JLO";
console.log(firstName + " " + lastName);
